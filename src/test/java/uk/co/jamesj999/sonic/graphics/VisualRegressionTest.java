    /**
     * Render the level at the specified position and compare against reference image.
     * Uses checkpoint position for deterministic camera placement with visible objects.
     */
    private void assertScreenshotMatchesReference(int zone, int act, int playerX, int playerY, String filename)
            throws Exception {

        assumeTrue("Reference file not found: " + filename, referenceFileExists(filename));

        // Load reference image
        BufferedImage reference = loadReferenceImage(filename);
        assertNotNull("Reference image should load: " + filename, reference);

        // Render current output
        BufferedImage current = renderScreenshot(zone, act, playerX, playerY);

        // Compare images
        ScreenshotCapture.ComparisonResult result = ScreenshotCapture.imagesMatch(reference, current, PIXEL_TOLERANCE);

        if (!result.matched()) {
            // Save diff image for debugging
            saveDiffImage(filename, reference, current);

            fail(String.format(
                    "Visual mismatch for %s: %d pixels differ (max diff=%d), first diff at (%d, %d). " +
                    "Diff image saved to target/visual-diff/",
                    filename, result.diffCount(), result.maxDiff(), result.firstDiffX(), result.firstDiffY()
            ));
        }

        System.out.println("Visual match verified for " + filename);
    }

    /**
     * Render a screenshot of the level at the specified player position.
     */
    private BufferedImage renderScreenshot(int zone, int act, int playerX, int playerY) throws IOException {
        LevelManager levelManager = LevelManager.getInstance();
        Camera camera = Camera.getInstance();
        GraphicsManager graphicsManager = GraphicsManager.getInstance();
        SpriteManager spriteManager = SpriteManager.getInstance();

        // Clear all sprites to avoid state pollution from other tests
        // (other tests may leave mock sprites with empty sensor arrays)
        spriteManager.clearAllSprites();

        // Reset parallax state to ensure deterministic rendering
        // (forces HTZ cloudCounter to reset, etc.)
        ParallaxManager.getInstance().resetZoneState();

        // Create a fresh player sprite
        SonicConfigurationService configService = SonicConfigurationService.getInstance();
        String mainCode = configService.getString(SonicConfiguration.MAIN_CHARACTER_CODE);
        AbstractPlayableSprite player = new Sonic(mainCode, (short) playerX, (short) playerY);
        spriteManager.addSprite(player);
        camera.setFocusedSprite(player);

        // Load level
        levelManager.loadZoneAndAct(zone, act);

        // Move player to checkpoint position
        player.setX((short) playerX);
        player.setY((short) playerY);

        // Force camera to snap to player position (no smooth scrolling)
        camera.updatePosition(true);

        // Spawn objects within camera view
        levelManager.updateObjectPositions();

        // Update ring spawning within camera view
        if (levelManager.getRingManager() != null) {
            levelManager.getRingManager().update(camera.getX(), player, 0);
        }

        // Update sprite animation to set correct mapping frame
        // (default frame 0 is intentionally empty; WAIT animation starts at frame 15)
        SpriteManager.getInstance().updateWithoutInput();

        // Clear screen with level's background color
        levelManager.setClearColor();
        glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

        // Render level with sprites
        levelManager.drawWithSpritePriority(SpriteManager.getInstance());

        // Flush all queued commands
        graphicsManager.flush();

        // Ensure all GL commands are complete
        glFinish();

        // Capture framebuffer
        return ScreenshotCapture.captureFramebuffer(SCREEN_WIDTH, SCREEN_HEIGHT);
    }

    /**
     * Save a diff image showing the differences between reference and current.
     */
    private void saveDiffImage(String originalFilename, BufferedImage reference, BufferedImage current)
            throws IOException {
        Path diffDir = Paths.get("target", "visual-diff");
        Files.createDirectories(diffDir);

        // Save current image
        String currentFilename = originalFilename.replace(".png", "_current.png");
        Path currentPath = diffDir.resolve(currentFilename);
        ScreenshotCapture.savePNG(current, currentPath);

        // Save diff image
        BufferedImage diff = ScreenshotCapture.createDiffImage(reference, current, PIXEL_TOLERANCE);
        String diffFilename = originalFilename.replace(".png", "_diff.png");
        Path diffPath = diffDir.resolve(diffFilename);
        ScreenshotCapture.savePNG(diff, diffPath);

        System.out.println("Saved diff images to: " + diffDir.toAbsolutePath());
    }

    /**
     * Check if a reference file exists.
     */
    private static boolean referenceFileExists(String filename) {
        try (InputStream is = VisualRegressionTest.class.getClassLoader()
                .getResourceAsStream(REFERENCE_DIR + "/" + filename)) {
            return is != null;
        } catch (IOException e) {
            return false;
        }
    }

    /**
     * Load a reference image from resources.
     */
    private BufferedImage loadReferenceImage(String filename) throws IOException {
        // Try loading from classpath first
        try (InputStream is = getClass().getClassLoader()
                .getResourceAsStream(REFERENCE_DIR + "/" + filename)) {
            if (is == null) {
                // Fall back to file system
                Path filePath = Paths.get("src/test/resources", REFERENCE_DIR, filename);
                if (!Files.exists(filePath)) {
                    return null;
                }
                return ScreenshotCapture.loadPNG(filePath);
            }

            // Copy to temp file for ImageIO (works better with streams)
            File tempFile = File.createTempFile("visual_ref_", ".png");
            tempFile.deleteOnExit();
            Files.copy(is, tempFile.toPath(), java.nio.file.StandardCopyOption.REPLACE_EXISTING);
            return ScreenshotCapture.loadPNG(tempFile.toPath());
        }
    }
